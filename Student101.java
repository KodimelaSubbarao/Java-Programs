public class Student101
{
	private String name;
	private int marks;
	public Student101(String name,int marks)
	{
		this.name=name;
		this.marks=marks;
	}
	public String getName()
	{
		return this.name;
	}
	public int getMarks()
	{
		return this.marks;
	}
	public String toString()
	{
		return this.name;
	}
}
